import turtle as t
t.bgcolor('white')
t.pencolor('black')
t.penup()
t.goto(0,-300)
t.pendown()
t.begin_fill()
t.fillcolor('red')
t.circle(300)
t.end_fill()
t.penup()
t.goto(0,-240)
t.pendown()
t.begin_fill()
t.fillcolor('white')
t.circle(240)
t.end_fill()
t.penup()
t.goto(0,-140)
t.pendown()
t.begin_fill()
t.fillcolor('red')
t.circle(140)
t.end_fill()
t.penup()
t.goto(0,-100)
t.pendown()
t.begin_fill()
t.fillcolor('blue')
t.circle(100)
t.end_fill()

t.done()
